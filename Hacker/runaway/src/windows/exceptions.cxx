#include "exceptions.hxx"

void map_to_exception(int status)
{
	switch (status) {  // clang-format off
	case WSA_INVALID_HANDLE: throw WsaInvalidHandle();
	case WSA_NOT_ENOUGH_MEMORY: throw WsaNotEnoughMemory();
	case WSA_INVALID_PARAMETER: throw WsaInvalidParameter();
	case WSA_OPERATION_ABORTED: throw WsaOperationAborted();
	case WSA_IO_INCOMPLETE: throw WsaIoIncomplete();
	case WSA_IO_PENDING: throw WsaIoPending();
	case WSAEINTR: throw WsaEIntr();
	case WSAEBADF: throw WsaEBadF();
	case WSAEACCES: throw WsaEAcces();
	case WSAEFAULT: throw WsaEFault();
	case WSAEINVAL: throw WsaEInval();
	case WSAEMFILE: throw WsaEMFile();
	case WSAEWOULDBLOCK: throw WsaEWouldBlock();
	case WSAEINPROGRESS: throw WsaEInProgress();
	case WSAEALREADY: throw WsaEAlready();
	case WSAENOTSOCK: throw WsaENotSock();
	case WSAEDESTADDRREQ: throw WsaEDestAddrReq();
	case WSAEMSGSIZE: throw WsaEMsgSize();
	case WSAEPROTOTYPE: throw WsaEProtoType();
	case WSAENOPROTOOPT: throw WsaENoProtoOpt();
	case WSAEPROTONOSUPPORT: throw WsaEProtoNoSupport();
	case WSAESOCKTNOSUPPORT: throw WsaESockTNoSupport();
	case WSAEOPNOTSUPP: throw WsaEOpNotSupp();
	case WSAEPFNOSUPPORT: throw WsaEPFNoSupport();
	case WSAEAFNOSUPPORT: throw WsaEAFNoSupport();
	case WSAEADDRINUSE: throw WsaEAddrInUse();
	case WSAEADDRNOTAVAIL: throw WsaEAddrNotAvail();
	case WSAENETDOWN: throw WsaENetDown();
	case WSAENETUNREACH: throw WsaENetUnreach();
	case WSAENETRESET: throw WsaENetReset();
	case WSAECONNABORTED: throw WsaEConnAborted();
	case WSAECONNRESET: throw WsaEConnReset();
	case WSAENOBUFS: throw WsaENoBufS();
	case WSAEISCONN: throw WsaEIsConn();
	case WSAENOTCONN: throw WsaENotConn();
	case WSAESHUTDOWN: throw WsaEShutdown();
	case WSAETOOMANYREFS: throw WsaETooManyRefs();
	case WSAETIMEDOUT: throw WsaETimedOut();
	case WSAECONNREFUSED: throw WsaEConnRefused();
	case WSAELOOP: throw WsaELoop();
	case WSAENAMETOOLONG: throw WsaENameTooLong();
	case WSAEHOSTDOWN: throw WsaEHostDown();
	case WSAEHOSTUNREACH: throw WsaEHostUnreach();
	case WSAENOTEMPTY: throw WsaENotEmpty();
	case WSAEPROCLIM: throw WsaEProcLim();
	case WSAEUSERS: throw WsaEUsers();
	case WSAEDQUOT: throw WsaEDQuot();
	case WSAESTALE: throw WsaEStale();
	case WSAEREMOTE: throw WsaERemote();
	case WSASYSNOTREADY: throw WsaSysNotReady();
	case WSAVERNOTSUPPORTED: throw WsaVerNotSupported();
	case WSANOTINITIALISED: throw WsaNotInitialized();
	case WSAEDISCON: throw WsaEDiscon();
	case WSAENOMORE: throw WsaENoMore();
	case WSAECANCELLED: throw WsaECancelled();
	case WSAEINVALIDPROCTABLE: throw WsaEInvalidProcTable();
	case WSAEINVALIDPROVIDER: throw WsaEInvalidProvider();
	case WSAEPROVIDERFAILEDINIT: throw WsaEProviderFailedInit();
	case WSASYSCALLFAILURE: throw WsaSysCallFailure();
	case WSASERVICE_NOT_FOUND: throw WsaServiceNotFound();
	case WSATYPE_NOT_FOUND: throw WsaTypeNotFound();
	case WSA_E_NO_MORE: throw WsaENoMore2();
	case WSA_E_CANCELLED: throw WsaECancelled2();
	case WSAEREFUSED: throw WsaERefused();
	case WSAHOST_NOT_FOUND: throw WsaHostNotFound();
	case WSATRY_AGAIN: throw WsaTryAgain();
	case WSANO_RECOVERY: throw WsaNoRecovery();
	case WSANO_DATA: throw WsaNoData();
	case WSA_QOS_RECEIVERS: throw WsaQosReceivers();
	case WSA_QOS_SENDERS: throw WsaQosSenders();
	case WSA_QOS_NO_SENDERS: throw WsaQosNoSenders();
	case WSA_QOS_NO_RECEIVERS: throw WsaQosNoReceivers();
	case WSA_QOS_REQUEST_CONFIRMED: throw WsaQosRequestConfirmed();
	case WSA_QOS_ADMISSION_FAILURE: throw WsaQosAdmissionFailure();
	case WSA_QOS_POLICY_FAILURE: throw WsaQosPolicyFailure();
	case WSA_QOS_BAD_STYLE: throw WsaQosBadStyle();
	case WSA_QOS_BAD_OBJECT: throw WsaQosBadObject();
	case WSA_QOS_TRAFFIC_CTRL_ERROR: throw WsaQosTrafficCtrlError();
	case WSA_QOS_GENERIC_ERROR: throw WsaQosGenericError();
	case WSA_QOS_ESERVICETYPE: throw WsaQosEServiceType();
	case WSA_QOS_EFLOWSPEC: throw WsaQosEFlowspec();
	case WSA_QOS_EPROVSPECBUF: throw WsaQosEProvSpecBuf();
	case WSA_QOS_EFILTERSTYLE: throw WsaQosEFilterStyle();
	case WSA_QOS_EFILTERTYPE: throw WsaQosEFilterType();
	case WSA_QOS_EFILTERCOUNT: throw WsaQosEFilterCount();
	case WSA_QOS_EOBJLENGTH: throw WsaQosEObjLength();
	case WSA_QOS_EFLOWCOUNT: throw WsaQosEFlowCount();
	case WSA_QOS_EUNKNOWNPSOBJ: throw WsaQosEUnknownPsObj();
	case WSA_QOS_EPOLICYOBJ: throw WsaQosEPolicyObj();
	case WSA_QOS_EFLOWDESC: throw WsaQosEFlowDesc();
	case WSA_QOS_EPSFLOWSPEC: throw WsaQosEPsFlowspec();
	case WSA_QOS_EPSFILTERSPEC: throw WsaQosEPsFilterSpec();
	case WSA_QOS_ESDMODEOBJ: throw WsaQosESDModeObj();
	case WSA_QOS_ESHAPERATEOBJ: throw WsaQosEShapeRateObj();
	case WSA_QOS_RESERVED_PETYPE: throw WsaQosReservedPEType();
	default: throw exception();
	}  // clang-format on
}